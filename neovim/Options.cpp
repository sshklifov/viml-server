StaticStringMap<VarType> creator();

VarType get_option_type(const char* name, int n) {
    static const StaticStringMap<VarType> options = creator();

    if (n == 4 && name[0] == 't' && name[1] == '_' && name[2] != NUL && name[3] != NUL) {
        return VAR_STRING; // t_xx/termcap option
    }

    int pos = options.find(name, n);
    if (pos < 0) {
        return VAR_UNKNOWN;
    }
    return options[pos];
}

